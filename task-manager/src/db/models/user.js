const mongoose = require('mongoose');
const validator = require('validator');
const bcrypt = require('bcryptjs')

//for object creation, the second argument that we pass to the model itself
//it internally converts the model to a schema

const userSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        unique: true,
        required: true,
        trim: true,
        lowercase: true,
        validate(value) {
            if (!validator.isEmail(value)) {
                throw new Error('Enter valid email');
            }
        }
    },
    age: {
        type: Number,
        default: 0,
        validate(value) {
            if (value < 0) {
                throw new Error('Age must be a positive number');
            }
        }
    },
    password: {
        type: String,
        required: true,
        trim: true,
        validate(value) {
            if (value.length < 7) {
                throw new Error('Password length to be greater than 6');
            }
            else if (validator.contains(value, 'password', { ignoreCase: true })) {
                throw new Error('Password cant contain the word password');
            }
        }
    }
})

